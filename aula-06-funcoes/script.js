//tipo funcoes

//declarativa
function funcao1() {
    console.log('SSSS')
}
funcao1();

//expressoes de função

//com nomeçao
var funcao2 = function funcao2() {
    console.log('NNNNN')
}
funcao2();

//sem nomeçao
var funcao3 = function() {
    console.log('NNNNN')
}
funcao3();

//arrow funcition
var funcao4 = () => {
    console.log('Sou uma arrow function')
}
funcao4();

